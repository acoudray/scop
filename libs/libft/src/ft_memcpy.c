#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*tab;
	const char	*tabs;
	size_t		i;

	tab = dst;
	tabs = src;
	i = 0;
	while (i < n)
	{
		tab[i] = tabs[i];
		i++;
	}
	return (dst);
}
