#include "libft.h"

size_t	ft_strclen(const char *s, char c)
{
	int i;

	if (s == 0)
		return (0);
	i = 0;
	while (*(s + i) && *(s + i) != c)
		i++;
	if (*(s + i) != c)
		i = 0;
	return (i);
}
