 #include "mat4lib.h"

t_mat4 mat4_trans(t_vect3 v)
{
    t_mat4 new;

    new = mat4_idset();
    new.m[3] = v.v[X];
    new.m[7] = v.v[Y];
    new.m[11] = v.v[Z];
    new.m[15] = 1;
    return (new);
}