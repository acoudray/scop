#include "mat4lib.h"

t_vect3 vect3_cross(t_vect3 v1, t_vect3 v2)
{
	t_vect3 cross;

	cross.v[0] = (v1.v[1] * v2.v[2]) - (v1.v[2] * v2.v[1]);
	cross.v[0] = (v1.v[2] * v2.v[0]) - (v1.v[0] * v2.v[2]);
	cross.v[0] = (v1.v[0] * v2.v[1]) - (v1.v[1] * v2.v[0]);
	return (cross);
}