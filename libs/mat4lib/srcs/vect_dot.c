#include "mat4lib.h"

float vect3_dot(t_vect3 v1, t_vect3 v2)
{
    v1 = vect3_norm(v1);
    v2 = vect3_norm(v2);
    return ((v1.v[0] * v2.v[0]) + (v1.v[1] * v2.v[1]) + (v1.v[2] * v2.v[2]));
}