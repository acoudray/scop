#include "mat4lib.h"

t_vect4	matvect_mul(t_mat4 m, t_vect4 v)
{
	int		i;
	int		j;
	t_vect4	new;

	i = -1;
	j = -1;
	while (++i < 16)
	{
		j += ((i % 4) == 0) ? 1 : 0;
		new.v[j] += m.m[i] * v.v[i % 4];
	}
	return (new);
}
